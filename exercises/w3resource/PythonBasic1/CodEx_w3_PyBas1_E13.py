# Exercise 13:

# Problem Statement:

# Write a Python program to print the following 'here document' or 'heredoc':
# Chop 'em if they're taller.
# Stomp 'em if they're smaller.
# Mauler, brawler, legacy hauler,
# smoke that crow, earn his holler.
# Mauler, brawler, legac hauler,
# smoke that ant, pay off your collar.
# Legio!
# Aeterna!
# Victrix!


# Solution Attempt:

# Print the heredoc using a multi-line string
print("""
      Chop 'em if they're taller.
      Stomp 'em if they're smaller.
      Mauler, brawler, legacy hauler,
      smoke that crow, earn his holler.
      Mauler, brawler, legac hauler,
      smoke that ant, pay off your collar.
      Legio!
      Aeterna!
      Victrix!)
      """)


# Lessons Learned:

# Syntax:
# print(""" """): Prints the specified text as a multi-line/heredoc string, preserving line breaks, indents, and other whitespace